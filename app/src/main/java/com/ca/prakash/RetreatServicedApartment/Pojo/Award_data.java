public class Award_data {
    String award;
    String image;

    public Award_data(String award, String image) {
        this.award = award;
        this.image = image;
    }

    public String getAward() {
        return award;
    }

    public void setAward(String award) {
        this.award = award;
    }

    public String getImage() {
        return image;
    }

    public void setImage(String image) {
        this.image = image;
    }
}


